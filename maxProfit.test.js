import maxProfit from './maxProfit'

// [] = 0
// [5] = 0
// [1, 3] = 2
// [1, 2, 3] = 2
// [1, 1, 2, 3] = 2
// [3, 1, 2, 3, 4] = 3
// [7, 6, 4, 3, 1] = 0
// [3, 5, 4, 7] = 4

test.each(
  [
    [[], 0],
    [[5], 0],
    [[1, 3], 2],
    [[1, 2, 3], 2],
    [[1, 1, 2, 3], 2],
    [[3, 1, 2, 3, 4], 3],
    [[7, 1, 5, 3, 6, 4], 5],
    [[7, 6, 4, 3, 1], 0],
    [[3, 2, 6, 5, 0, 3], 4],
    [[1, 2, 4, 7, 11], 10],
    [[4, 7, 1, 2], 3],
    [[2, 1, 2, 1, 0, 1, 2], 2]
  ]
)('array of %p yield max profit of %i', (prices, profit) => {
  expect(maxProfit(prices)).toBe(profit)
})